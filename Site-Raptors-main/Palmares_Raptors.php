    <h4>Fondation des Raptors de Toronto</h4>
    <p class="Text">Les Raptors de Toronto constituent l’une des deux équipes d’expansion qui se joignent à la NBA lors de la saison
        1995-1996, l’autre étant également canadienne (les Grizzlies de Vancouver). Il s’agit d’un jalon important pour
        la NBA, qui ne s’est jusqu’alors jamais aventurée au-delà des frontières des États-Unis.
        Deux ans auparavant, en avril 1993, le groupe Professional Basketball Franchise (Canada) Inc. (PBF) dépose une
        demande officielle d’acquisition d’une équipe de la NBA. Celle-ci est préparée par John Bitove, président d’une
        société de services financiers, et d’un groupe comprenant l’ancien premier ministre de l’Ontario David Peterson
        et l’ancienne vedette des Pistons de Detroit, Isiah Thomas, qui deviendra plus tard le premier vice-président
        des Raptors.

        En juillet 1993, un comité d’expansion de la NBA se rend à Toronto. Le groupe PBF lui présente son plan de
        construction d’un aréna au centre-ville, devant être relié à une ligne du métro de Toronto. Le 30 septembre
        1993, PBF se voit attribuer conditionnellement une franchise à compter de la saison 1995-1996, laquelle
        deviendra la 28e équipe de la NBA, et ce, moyennant des frais d’expansion alors sans précédent de 125 millions
        de dollars. En vertu de l’accord, Toronto doit jouer ses deux premières saisons au SkyDome (maintenant le Rogers
        Center), en attendant l’achèvement de son domicile.</p>
